namespace Blackajte\TraitsBundle\Traits\Identifiable;

use Doctrine\ORM\Mapping as ORM;

trait IdentifiableTrait
{
    protected $id;
    
    /**
     * {@inheritDoc}
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * {@inheritDoc}
     */
    public function setId($id)
    {
        $this->id = $id;
        return $this;
    }
}
